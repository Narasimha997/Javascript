// Greater Than (>) or Equals to(>=)
let a=5;
let b=4;
console.log(a>b);
// In the output we got true so a is greater than b
/* The greater than operator returns true if the left-hand side is greater than the righthand
side of the operation. We use the > character for this*/
// Greater than or equal to(>=)
a=6;
b=5;
console.log(a>=b);
/* >= this operator which returns true if the left-hand side is greater than
or equal to the right-hand side.*/
